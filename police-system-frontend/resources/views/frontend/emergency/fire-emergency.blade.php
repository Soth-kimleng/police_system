<h1>Emergency: Fire Emergency</h1>

<h2>Preventing Fires</h2>
<p>Fire prevention is essential in ensuring the safety of individuals and properties. Here are some fire prevention
    tips:</p>
<ul>
    <li>Install smoke detectors and regularly check their batteries</li>
    <li>Keep flammable materials away from heat sources</li>
    <li>Do not leave cooking unattended</li>
    <li>Properly extinguish cigarettes and dispose of them safely</li>
    <li>Avoid overloading electrical outlets and use electrical appliances responsibly</li>
    <li>Maintain and inspect electrical wiring and systems regularly</li>
</ul>

<h2>What to Do in a Fire Emergency</h2>
<p>If a fire breaks out, follow these steps:</p>
<ol>
    <li>Stay calm and alert.</li>
    <li>Immediately evacuate the building or area, following the designated escape routes.</li>
    <li>If possible, close doors and windows to slow down the spread of fire and smoke.</li>
    <li>Do not use elevators. Use stairs to evacuate.</li>
    <li>Alert others and assist anyone who may need help, especially individuals with disabilities or limited mobility.
    </li>
    <li>Once outside, move to a safe location away from the fire.</li>
    <li>Call the fire department emergency hotline at <strong>118</strong> to report the fire.</li>
    <li>Do not re-enter the building until it has been declared safe by the fire department.</li>
</ol>

<h2>Fire Safety Equipment</h2>
<p>Fire safety equipment plays a crucial role in managing fire emergencies. Here are some essential fire safety
    equipment:</p>
<ul>
    <li>Fire extinguishers: Learn how to use a fire extinguisher and have one readily accessible in your home or
        workplace.</li>
    <li>Fire blankets: Use fire blankets to smother small fires or wrap around yourself if your clothes catch fire.</li>
    <li>Fire alarms: Install smoke detectors and fire alarms in key areas to provide early warning of fire.</li>
    <li>Fire escape ladders: If you live or work in a multi-story building, consider having fire escape ladders
        available as an additional escape route.</li>
</ul>

<h2>Fire Safety Training</h2>
<p>Consider attending fire safety training programs or workshops to learn essential fire safety techniques, including
    fire extinguisher usage and evacuation procedures. These skills can help you respond effectively in a fire
    emergency.</p>

<h2>Contacting the Fire Department</h2>
<p>In case of a fire emergency, call the fire department emergency hotline at <strong>118</strong>. Provide clear and
    concise information about the location and nature of the fire to facilitate a swift response.</p>

