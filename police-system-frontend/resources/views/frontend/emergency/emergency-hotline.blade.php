<h1>Emergency: Emergency Hotline</h1>

<h2>Importance of Emergency Hotline</h2>
<p>An emergency hotline is a vital resource in times of crisis or when immediate assistance is required. It provides a
    direct and reliable means of contacting the appropriate authorities for emergency response and support.</p>

<h2>Emergency Hotline Numbers in Cambodia</h2>
<p>In Cambodia, the following emergency hotline numbers are available:</p>
<ul>
    <li>
        <h3>Police: 117</h3>
        <p>The police emergency hotline is available 24/7 to report crimes, emergencies, or seek immediate police
            assistance.</p>
    </li>
    <li>
        <h3>Fire Department: 118</h3>
        <p>The fire department emergency hotline should be contacted in case of fire emergencies or when rescue services
            are needed.</p>
    </li>
    <li>
        <h3>Medical Emergencies: 119</h3>
        <p>The medical emergencies hotline connects you to emergency medical services for immediate medical assistance
            and ambulance services.</p>
    </li>
</ul>

<h2>When to Call the Emergency Hotline</h2>
<p>You should call the emergency hotline numbers in Cambodia in situations such as:</p>
<ul>
    <li>Reporting a crime or suspicious activity</li>
    <li>Requesting immediate police assistance</li>
    <li>Reporting a fire or requesting rescue services</li>
    <li>Seeking urgent medical assistance or ambulance services</li>
    <li>Reporting accidents or emergencies on roads</li>
    <li>Reporting other life-threatening situations requiring immediate attention</li>
</ul>

<h2>Tips for Calling the Emergency Hotline</h2>
<p>When calling the emergency hotline, follow these guidelines:</p>
<ul>
    <li>Remain calm and speak clearly</li>
    <li>Provide your name, location, and contact number</li>
    <li>Describe the nature of the emergency or situation concisely</li>
    <li>Follow the instructions provided by the operator</li>
    <li>Do not hang up until the operator tells you to do so</li>
    <li>Cooperate fully with emergency responders and authorities</li>
</ul>

<p>Remember, the emergency hotline is strictly for emergencies and immediate assistance. It is essential to use the
    hotline responsibly and avoid making false or prank calls that could divert resources from genuine emergencies.</p>
