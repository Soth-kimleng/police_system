<h1>Emergency: Medical Transport</h1>

<h2>When to Request Medical Transport</h2>
<p>Medical transport services are utilized when a patient needs transportation to a medical facility for non-emergency
    medical care or when the patient's condition requires specialized medical attention during transport. Some
    situations that may require medical transport include:</p>
<ul>
    <li>Transfers between medical facilities for specialized treatment or higher level of care</li>
    <li>Transportation for medical appointments, tests, or procedures</li>
    <li>Inter-hospital transfers</li>
    <li>Transportation for individuals with limited mobility or medical conditions that require assistance during
        transport</li>
    <li>Medical repatriation for individuals returning to Cambodia for medical treatment</li>
</ul>

<h2>Types of Medical Transport</h2>
<p>In Cambodia, there are different types of medical transport available based on the patient's needs:</p>
<ul>
    <li><strong>Ambulance Services:</strong> Ambulances are equipped with medical equipment and staffed by trained
        professionals who can provide medical care during transport. They are used for emergency and non-emergency
        medical transport.</li>
    <li><strong>Medical Taxis:</strong> Medical taxis are specially equipped vehicles that provide non-emergency medical
        transport for individuals who do not require extensive medical care during the journey.</li>
    <li><strong>Air Medical Transport:</strong> In cases of critical emergencies or when long-distance transportation is
        required, air medical transport by helicopter or fixed-wing aircraft may be arranged to quickly and safely
        transport patients to appropriate medical facilities.</li>
</ul>

<h2>How to Request Medical Transport</h2>
<p>If you require medical transport, follow these steps:</p>
<ol>
    <li>Call the medical transport hotline at <strong>119</strong> to request medical transport.</li>
    <li>Provide clear and accurate information about the patient's condition, location, and the type of medical
        transport needed.</li>
    <li>Follow any instructions provided by the medical transport operator and remain on the line until help arrives.
    </li>
    <li>If the situation is critical or life-threatening, call the emergency hotline at <strong>119</strong> instead for
        immediate assistance.</li>
</ol>

<h2>Costs and Coverage</h2>
<p>The costs of medical transport may vary depending on the type of service, distance, and medical requirements. It's
    important to check with your medical insurance provider or the medical transport service for information about
    coverage and any associated costs.</p>

