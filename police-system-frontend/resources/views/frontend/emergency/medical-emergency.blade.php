<h1>Emergency: Medical Emergency</h1>

<h2>Recognizing a Medical Emergency</h2>
<p>A medical emergency refers to a situation that requires immediate medical attention to prevent further harm or loss
    of life. The following signs and symptoms may indicate a medical emergency:</p>
<ul>
    <li>Sudden severe chest pain or difficulty breathing</li>
    <li>Uncontrolled bleeding or deep wounds</li>
    <li>Loss of consciousness or fainting</li>
    <li>Sudden paralysis or inability to move</li>
    <li>Seizures or convulsions</li>
    <li>Severe allergic reactions with difficulty breathing and swelling</li>
    <li>Signs of a stroke (e.g., slurred speech, sudden numbness or weakness on one side of the body)</li>
    <li>Serious burns or scalds</li>
    <li>Persistent vomiting or diarrhea leading to dehydration</li>
</ul>

<h2>What to Do in a Medical Emergency</h2>
<p>If you encounter a medical emergency, take the following steps:</p>
<ol>
    <li>Remain calm and assess the situation.</li>
    <li>If it's safe to do so, provide any necessary first aid or immediate care.</li>
    <li>Call the medical emergency hotline at <strong>119</strong> to report the emergency and request an ambulance.
    </li>
    <li>Provide clear and concise information about the location, nature of the emergency, and the condition of the
        patient.</li>
    <li>Follow any instructions provided by the emergency operator while help is on the way.</li>
    <li>Administer CPR or basic life support techniques if trained and necessary.</li>
    <li>Do not move the patient unless their safety is at immediate risk.</li>
    <li>Stay with the patient until medical professionals arrive.</li>
</ol>

<h2>Medical Facilities and Hospitals</h2>
<p>In Cambodia, there are several medical facilities and hospitals that provide emergency medical services. It's
    important to be aware of the nearest medical facilities in your area and their contact information for quick access
    in case of a medical emergency.</p>

<h2>Medical Emergency Hotline</h2>
<p>In a medical emergency, call the medical emergency hotline at <strong>119</strong>. The emergency operator will guide
    you and dispatch an ambulance to your location.</p>

<p>Please note that the information provided on this website is for general informational purposes only and should not
    be considered as a substitute for official guidelines or professional medical advice. It's crucial to follow the
    official instructions and procedures provided by the relevant authorities in Cambodia for medical emergencies and
    seek medical attention from qualified healthcare professionals.</p>
