<h1>Contact: Fire Fighter</h1>

<h2>Emergency Hotline for Fire</h2>
<p>In case of a fire emergency, immediately dial the national emergency hotline at <strong>118</strong> to report the
    fire and request assistance from the fire department. The emergency operator will gather information about the fire
    incident and dispatch fire fighters to the location.</p>

<h2>Contacting the Fire Department</h2>
<p>Aside from using the emergency hotline, you can also contact the local fire department directly for non-emergency
    inquiries or to report fire safety concerns. Here are some useful contact details:</p>

<ul>
    <li><strong>Fire Department Headquarters:</strong> Contact the fire department headquarters for general inquiries,
        fire safety information, and non-emergency concerns.</li>
    <li><strong>Local Fire Stations:</strong> Each city or province in Cambodia has its own fire stations. Contact the
        local fire station nearest to your location for non-emergency inquiries or to report fire safety concerns
        specific to your area.</li>
</ul>

<h2>Tips for Fire Safety</h2>
<p>Preventing fires and being prepared for fire emergencies is essential. Here are some fire safety tips:</p>

<ul>
    <li>Install smoke detectors on every floor of your home or building and test them regularly.</li>
    <li>Keep flammable materials away from heat sources and open flames.</li>
    <li>Ensure proper maintenance of electrical wiring and appliances to prevent electrical fires.</li>
    <li>Never leave cooking unattended and keep flammable items away from the stove.</li>
    <li>Develop and practice a fire escape plan with your family or colleagues.</li>
    <li>Learn how to use fire extinguishers and keep them easily accessible.</li>
    <li>In case of a fire, evacuate immediately and call the emergency hotline.</li>
</ul>

