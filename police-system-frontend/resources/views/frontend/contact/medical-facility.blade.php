<h2>Types of Medical Facilities</h2>
<p>In Cambodia, there are various types of medical facilities that provide healthcare services to the public. These
    include:</p>

<ul>
    <li><strong>Hospitals:</strong> Hospitals are equipped with advanced medical facilities, specialized departments,
        and a wide range of medical services. They offer both emergency and non-emergency medical care.</li>
    <li><strong>Clinics:</strong> Clinics provide medical services for non-emergency cases, routine check-ups, and
        general healthcare needs.</li>
    <li><strong>Specialized Centers:</strong> Specialized centers focus on specific medical areas such as cardiology,
        neurology, orthopedics, and more, providing specialized care and treatments.</li>
    <li><strong>Pharmacies:</strong> Pharmacies offer medications, over-the-counter drugs, and health-related products.
    </li>
</ul>

<h2>Contacting Medical Facilities</h2>
<p>If you need to contact a medical facility in Cambodia, here are some ways to get in touch:</p>

<ul>
    <li><strong>Emergency Hotline:</strong> If you require immediate medical assistance, call the national emergency
        hotline at <strong>119</strong> to request an ambulance or emergency medical services.</li>
    <li><strong>Local Hospitals and Clinics:</strong> Each city or province in Cambodia has its own hospitals and
        clinics. You can find their contact information in local directories or search online using specific keywords
        like "hospitals in [city/province name]" or "clinics near me."</li>
    <li><strong>Online Directories:</strong> Online directories or healthcare websites often provide a comprehensive
        list of medical facilities with their contact details, addresses, and specialties.</li>
</ul>

