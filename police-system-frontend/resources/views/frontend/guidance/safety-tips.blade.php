<h1>Guidance: Safety Tips</h1>

<h2>General Safety</h2>
<ul>
    <li>
        <h3>Be aware of your surroundings</h3>
        <p>Always stay alert and be aware of your surroundings, especially in unfamiliar areas. Trust your instincts and
            avoid potentially dangerous situations.</p>
    </li>
    <li>
        <h3>Secure your belongings</h3>
        <p>Keep your personal belongings, such as bags, wallets, and phones, secure at all times. Avoid displaying
            valuable items in public to minimize the risk of theft.</p>
    </li>
    <li>
        <h3>Use strong passwords</h3>
        <p>When creating passwords for online accounts, use a combination of uppercase and lowercase letters, numbers,
            and symbols. Avoid using easily guessable information, such as your name or birthdate.</p>
    </li>
</ul>

<h2>Online Safety</h2>
<ul>
    <li>
        <h3>Use secure websites</h3>
        <p>Ensure that websites you visit have secure connections (HTTPS) to protect your personal information. Avoid
            entering sensitive data on untrusted or unfamiliar websites.</p>
    </li>
    <li>
        <h3>Be cautious with personal information</h3>
        <p>Avoid sharing sensitive personal information, such as your social security number or bank account details,
            online unless necessary. Be wary of requests for personal information in unsolicited emails or messages.</p>
    </li>
    <li>
        <h3>Regularly update your software</h3>
        <p>Keep your computer, mobile devices, and software up to date with the latest security patches and updates.
            This helps protect against known vulnerabilities and exploits.</p>
    </li>
</ul>

<h2>Fire Safety</h2>
<ul>
    <li>
        <h3>Install smoke detectors</h3>
        <p>Make sure your home or workplace is equipped with functioning smoke detectors. Test them regularly and
            replace the batteries as needed.</p>
    </li>
    <li>
        <h3>Have a fire escape plan</h3>
        <p>Create and practice a fire escape plan with your family or colleagues. Identify multiple escape routes and a
            designated meeting point outside the building.</p>
    </li>
    <li>
        <h3>Keep flammable materials away</h3>
        <p>Avoid storing flammable materials near heat sources and ensure they are properly stored and labeled. Follow
            safety guidelines when using open flames or electrical appliances.</p>
    </li>
</ul>

<p>These are just a few general safety tips. For more specific safety information or guidance related to your situation,
    consult with relevant authorities or professionals.</p>
@endsection